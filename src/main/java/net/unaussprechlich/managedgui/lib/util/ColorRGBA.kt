class ColorRGBA {

    var RED = 0f
    var GREEN = 0f
    var BLUE = 0f
    var ALPHA = 0f

    constructor() {
    }

    constructor(RED: Float, GREEN: Float, BLUE: Float, ALPHA: Float) {
        this.RED = RED
        this.GREEN = GREEN
        this.BLUE = BLUE
        this.ALPHA = ALPHA
    }
}
